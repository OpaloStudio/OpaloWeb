    <h1 class="titulo">Cumbre con Causa</h1>
    <p class="descripcion">
        Cumbre con causa se trata de un evento donde 15 empresarios poblanos escalaron cuatro de las montañas más altas
        de México, con el objetivo de recaudar fondos para apoyar a la Fundación Down de Puebla. Este proyecto se
        realizó en conjunto con Marcela Medel.

    </p>

    <p class="descripcion">
    Problemas detectados: 

    </p>

    <ul class="descripcion">
        <li>Tiempo muy limitado para la realización del diseño</li>
        <li>Generar empatía en el público</li>
        <li>Hacerlo recordable</li>
        <li>Expresar todo con pocos elementos </li>
    </ul>

    <p class="descripcion">
        Mostrar las caras sonrientes de los integrantes que son apoyados por esa noble Fundación fue un elemento
        determinante para desarrollar el material que se requería. Pues la sonrisa de un niño es algo que ninguna
        enfermedad puede arrebatar, por lo tanto, fue importante representarla en el logo para generar empatía con las
        personas que contribuyen.

    </p>

    <p class="descripcion">
        En el logotipo también se incluyó la representación de las cuatro montañas que el Grupo Proactivo Mexicano
        tenía como meta escalar para el recaudamiento de fondos. Las montañas diseñadas son un representante claro de
        la osadía de emprender tal hazaña, pero al ser empresarios los participantes se hizo alusión a las gráficas de
        crecimiento de una empresa. Términos con los que están familiarizados los participantes. Al juntar estos dos
        elementos, se logra un logotipo armonioso con imágenes duales, donde puedes apreciar una carita feliz por un
        lado y por otro, al mismo tiempo, las montañas con un lago y el sol.


    </p>

    <p class="descripcion">
        El diseño hace visible la parte humana del proyecto, para lograr empatizar con las personas y cumplir con el
        objetivo de la Fundación Down de Puebla. Tal hazaña fue lograda en tiempo récord, pues debido a ciertas
        circunstancias, se tuvieron sólo tres días para desarrollar toda la campaña. Se aportó un 50% de creación a la
        idea inicial, sin embargo, el diseño resultante ha proyectado hasta ahora un crecimiento del 70% desde
        entonces. Siendo que el 2 de marzo del 2016 terminaron la misión de escalar las 4 montañas y se llegó a la meta
        de recaudar los fondos necesarios.

    </p>



</div>