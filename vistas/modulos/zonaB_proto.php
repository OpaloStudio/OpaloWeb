            <p class="descripcion" id="uno">
                Es un maker space que busca introducir la cultura de “hazlo tú mismo” mediante la enseñanza de talleres
                de
                electrónica, arduinos, carpintería, diseño e impresión 3D. También es un punto de encuentro para
                desarrollar
                diversos proyectos o reparaciones, para darle una segunda vida a las piezas de aparatos que ya no
                sirven.

            </p>

            <p class="descripcion">
                El desarrollo de Protoplasma implicó la conceptualización completa del proyecto; se tuvo que concretar
                la
                estructura del modelo de negocios; la misión, visión y valores; se intervino y consolidó la identidad;
                y se
                consiguió realizar una representación gráfica sólida y memorable.
                <br><br id="dos">Sin embargo, al ser una materialización se detectaron los siguientes problemas:
            </p>

            <ul class="descripcion">
                <li>Branding incompleto</li>
                <li>Falta de una idea consolidada y sin estructura</li>
                <li>Explicar de manera sencilla a diversos públicos qué hace Protoplasma</li>
                <li>Diferenciarnos eficazmente de la competencia </li>
                <li>Posicionar a Protoplasma en la mente de los consumidores </li>
            </ul>

            <p class="descripcion" id="tres">
                Al presentarse a públicos tan jóvenes (de 15 a 25 años) que mayormente llegan sin conocimiento previo a
                los
                talleres que se imparten, era muy importante diseñar una identidad sólida que representara de manera
                llamativa
                los conceptos que se manejan. Lo logramos mediante la realización de representaciones gráficas
                (semiótica) que
                las personas pueden identificar fácilmente.
            </p>

            <p class="descripcion">
                Dicha iconografía funciona porque tiene como base todo un desarrollo de la identidad filosófica de la
                empresa:
                misión, visión y los valores que manejan. El cual fue conceptualizado mediante las metodologías que
                Ópalo
                implementa.

            </p>

            <p class="descripcion" id="cuatro">
                La correcta estructuración del modelo de negocios y de la logística también fue parte del trabajo
                desarrollado
                por el estudio. Lo cual es una parte crucial en la materialización de Protoplasma pues es lo que
                sostiene y
                define la calidad de sus servicios que marcan la diferencia con respecto a la competencia.

            </p>

            <p class="descripcion">
                Un aspecto determinante en la implementación del proyecto es que el público objetivo pueda saber de la
                existencia de Protoplasma y de las ideas que pueden plasmar ahí. Por lo tanto, se realizó una
                estrategia donde
                se integran diferentes maneras de aproximarnos al público objetivo. Con stickers, que fueron colocados
                tácticamente en puntos clave cercanos a el espacio y los usuarios. También nos aproximamos por redes
                sociales y
                mediante activaciones interesantes que demuestran visualmente lo que puedes lograr ahí.
            </p>

            <p class="descripcion" id="cinco">
                El tiempo de trabajo fue de tres meses, donde desarrollamos un 38% de la estructura inicial del
                proyecto. Este
                es considerado una materialización pues la idea inicial necesitaba ampliarse y concretarse.
            </p>




