<div class="info flexy">
    <h1 class="titulo">Alex Ferez</h1>
    <p class="descripcion">
        Ludicultura es una asociación civil cuyos valores e identidad trabajan para reparar el tejido
        social de México. Mediante la estimulación de redes de colaboración que activen, vinculen e integren a diversos
        actores sociales. Arte, cultura, diversión y ciencia, es un mensaje que con la participación de varios jóvenes
        se logra llevar a diversos públicos.
    </p>

    <p class="descripcion">
        Para el rediseño de esta asociación civil, se realizó un trabajo de campo que además de enriquecedor, nos
        permitió mantener la esencia de lo que representa Ludicultura. El proceso se realizó con una estrategia
        creativa que abarcó varios factores: conocer a las personas que son parte de la asociación, jugar con niños,
        estar en las dinámicas de trabajo, entre otras cosas. Como resultado de tal análisis se detectaron los
        siguientes problemas:
    </p>
    <ul class="descripcion">
    <li>Falta de una identidad clara</li>
    <li>Ilegible</li>
    <li>La identidad no lograba incluir a los usuarios de manera gráfica</li>
    <li>Logo poco recordable</li>
    <li>Difícil de reproducir</li>
    </ul>

    <p class="descripcion">
        Vivimos la experiencia Ludi para poder diseñar una identidad que estuviera a la altura de los valores y el
        trabajo de la organización, pero que también diera solución a los problemas detectados. Todo esto resultó en
        una identidad que brinda las soluciones y facilidades que un buen diseño puede aportar, así como la
        flexibilidad que necesita la organización para utilizar el logotipo en diversas maneras.

    </p>

    <p class="descripcion">
        Se utilizó una paleta de colores lúdica, cuyo juego tonal y conexión en forma de nodos, brinda un ejemplo
        visual de cómo se entrelaza la misión y los valores de la organización con la identidad. También se tuvieron en
        cuenta los sistemas de comunicación que Ludicultura empleaba con anterioridad, con la idea de mantener la
        esencia. Dicho desarrollo representa una aportación del 10% extra a concebir la estructura que ya tenían
        establecida.
    </p>
    <p class="descripcion">
        El logotipo incluye la diversidad y diversión, acompañada por el arte, un mensaje claro que las personas pueden
        identificar como Ludi. Elemento que era muy importante, pues no sólo buscábamos agradar a los integrantes, sino
        lograr que todas las personas que participan se sientan parte de la identidad. Tomar en cuenta estos factores
        se vió reflejado en el crecimiento que tuvo la organización luego de nuestra intervención. Ludicultura es un
        trabajo íntegro que se logró realizar en un corto periodo de tiempo y además refleja claramente que la
        experiencia con nuestros clientes también nos enriquece.

    </p>

</div>