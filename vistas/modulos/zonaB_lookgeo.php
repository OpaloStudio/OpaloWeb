        de qué trata el proyecto, dilucidar a donde se tiene que llevar y la manera de posicionarlo. Se detectaron los
        problemas principales y posibles soluciones:
    </p>

    <ul class="descripcion">
        <li>Ausencia de referentes que expongan las dificultades y facilidades de una plataforma como LookGeo en un
            público mexicano</li>
        <li>Falta de un concepto consolidado</li>
        <li>Superar plataformas existentes en otros países</li>
        <li>Ser la primera opción de los consumidores </li>
        <li>Lograr la participación de los ofertantes de bienes inmuebles </li>
    </ul>

    <p class="descripcion">
